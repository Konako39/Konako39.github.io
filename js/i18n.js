/* 语言选择：记忆 > 系统语言 > 英语 */
function detectLang() {
  try {
    const savedLang = localStorage.getItem("konako_lang");
    if (savedLang && I18N[savedLang]) return savedLang;
  } catch (e) {}
  const nav = (navigator.language || "en").toLowerCase();
  if (nav.startsWith("zh")) return "zh";
  if (nav.startsWith("ja")) return "ja";
  return "en";
}
const LANG = detectLang();

/* UI 文案查询，支持 {var} 插值 */
function T(key, vars) {
  let s = (I18N[LANG].ui[key] != null ? I18N[LANG].ui[key] : I18N.zh.ui[key]);
  if (s == null) s = key;
  if (vars) for (const k in vars) s = s.replaceAll("{" + k + "}", vars[k]);
  return s;
}

/* 项目字段本地化：优先当前语言的覆写，缺省回退 projects.js 原文 */
function LP(p, field) {
  const ov = I18N[LANG].projects && I18N[LANG].projects[p.id];
  if (ov && ov[field] != null) return ov[field];
  return p[field];
}
function LPMedia(p, idx) {
  const ov = I18N[LANG].projects && I18N[LANG].projects[p.id];
  if (ov && ov.mediaLabels && ov.mediaLabels[idx] != null) return ov.mediaLabels[idx];
  return p.media[idx].label;
}
